fun main() {
    var x=100
    var y=5
    println(x + y)
    println(x - y)
    println(x * y)
    println(x / y)
    println(x % y)//modulus

    //Comparison operators
    var  a = 45
    var  b = 3
    println(a < b)
    println(a > b)
    println(a <= b)
    println(a >=b)
    println(a == b)
    println(a != b)

    //Assignment operators
    var  number =90
    println(number)


    number += 2
    println(number)

    //Logical operators -&&,||,!
    var num =78
    println(num < 10 && num > 20)
    println(num < 10 || num > 20)
    println(!(num < 10 || num > 20))

}